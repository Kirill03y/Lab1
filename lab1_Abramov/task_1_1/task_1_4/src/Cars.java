public class Cars {
    String car_model;
    String reg_number;
    int year_manufactured;
    int car_mileage;
    int car_cost;

    public Cars(){

    }

    public Cars(String car_model, String reg_number, int year_manufactured, int car_mileage, int car_cost){
        this.car_model = car_model;
        this.reg_number = reg_number;
        this.year_manufactured = year_manufactured;
        this.car_mileage = car_mileage;
        this.car_cost = car_cost;
    }

}
